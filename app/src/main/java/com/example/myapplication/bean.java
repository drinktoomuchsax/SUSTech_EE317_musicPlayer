package com.example.myapplication;

public class bean {

    String name;
    String news;
    String time;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getNews() {
        return news;
    }

    public String getTime() {
        return time;
    }

    public void setNews(String news) {
        this.news = news;
    }

    public void setTime(String time) {
        this.time = time;
    }
}


